<div class="col-xs-12 col-sm-24 col-md-24 col-lg-24 Ast">
    <fieldset>
        <legend style="font-size:17px;">GAF</legend>
    </fieldset>
</div>
<div class="col-xs-12 col-sm-24 col-md-24 col-lg-24" align="justify" style="font-size:15px;">
    Le projet GAF a été réfléchi, tissé, conçu et mis en place pour permettre au peuple Burkinabé d'être à jour des informations sur les décisions et faits étatiques ou gouvernementaux. En effet, le peuple ne doit pas être à la berge des décisions adoptées lors des conseils de ministres, des textes de lois qui pèsent sur le pays et des rapports des institutions publiques qui se doivent d'être revelés.
</div>
<div class="col-xs-11 col-sm-22 col-md-22 col-lg-22 col-xs-offset-1 col-sm-offset-2 col-md-offset-2 col-lg-offset-2 Ast Act" style="font-size:16px;">
    <u>Problématique</u>
</div>
<div class="col-xs-11 col-sm-22 col-md-22 col-lg-22 col-xs-offset-1 col-sm-offset-2 col-md-offset-2 col-lg-offset-2" align="justify" style="font-size:15px;">
    Les 30 et 31 octobre 2014, suite à la tentative de modification de l’article 37 limitant les mandats présidentiels à deux, le peuple Burkinabè en une insurrection populaire a chassé du pouvoir l’ex président Blaise Compaoré.<br/>En 97 une même tentative, celle là réussie, est passée comme une lettre à la poste sur proposition de loi en conseil de ministres. la vigilance citoyenne était absente, les outils pour la mettre en exergue manquaient...
</div>
<div class="col-xs-11 col-sm-22 col-md-22 col-lg-22 col-xs-offset-1 col-sm-offset-2 col-md-offset-2 col-lg-offset-2 Ast Act" style="font-size:16px;">
    <u>Contexte</u>
</div>
<div class="col-xs-11 col-sm-22 col-md-22 col-lg-22 col-xs-offset-1 col-sm-offset-2 col-md-offset-2 col-lg-offset-2" align="justify" style="font-size:15px;">
    Le Burkina Faso est aujourd’hui dans une situation post-révolutionnaire. L’opinion publique s’est renforcée et exige transparence, justice et clarté dans les décisions. 71,3% de la population est analphabète. Les notions de redevabilité, de bonne gouvernance, de citoyenneté, de démocratie sont peu accessibles.
</div>
<div class="col-xs-11 col-sm-22 col-md-22 col-lg-22 col-xs-offset-1 col-sm-offset-2 col-md-offset-2 col-lg-offset-2 Ast Act" style="font-size:16px;">
    <u>Des données aux impacts :</u> Résultats et Opportunités
</div>
<div class="col-xs-11 col-sm-22 col-md-22 col-lg-22 col-xs-offset-1 col-sm-offset-2 col-md-offset-2 col-lg-offset-2" align="justify" style="font-size:15px;">
    Ci-dessous des formulations assez explicites pour qualifier les résultats et opportunités :
    <ul style="margin-bottom:0;">
        <li>Un outil simple qui permet de rechercher une information précise tout en contribuant à l’analyser, à mémoriser et rappeler une information, une échéance, et ainsi lancer une alerte lorsqu’elle est nécessaire ;</li>
        <li>Les populations comprennent mieux l’enjeu et le contexte des décisions qui sont prises, le contrôle citoyen est accru ;</li>
        <li>Les gouvernants se sentent redevables, les nominations ne sont plus complaisantes, les entreprises ne sont plus ultra-attributaires, la corruption diminue conséquemment.</li>
    </ul>
</div>
